print(10 / 3)  # Division (float)
print(10 // 3)  # Integer output (floor)
print(10 % 3)  # Modulus operator (remainder of division)
print(10 ** 3)  # Exponent (power)

x = 10
x += 3  # Augmented / enhanced assignment operator
print(x)

"""Operator precedence:
>Parenthesis
>Exponentiation
>Multiplication or Division
>Addition or Subtraction"""

y = 10 + 3 * 2 ** 2
print(y)
